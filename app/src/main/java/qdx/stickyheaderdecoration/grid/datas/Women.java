package qdx.stickyheaderdecoration.grid.datas;


public class Women {
    String name;
    String photo;
    String avatar;

    public Women(String name, String photo, String avatar) {
        this.name = name;
        this.photo = photo;
        this.avatar = avatar;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhoto() {
        return photo;
    }

    public void setPhoto(String photo) {
        this.photo = photo;
    }

    public String getAvatar() {
        return avatar;
    }

    public void setAvatar(String avatar) {
        this.avatar = avatar;
    }
}
